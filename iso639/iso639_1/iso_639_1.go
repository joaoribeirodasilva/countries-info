func GetID(code string) iso639.ID {

	val, ok := ids[ISO639_1(strings.ToLower(code))]
	if !ok {
		return ids[""]
	}
	return val
}

func GetCode(id iso639.ID) ISO639_1 {

	return iso639_1[id]
}
